import express from 'express';
import cors from 'cors';
import mongoose from 'mongoose';
import dotenv from 'dotenv';

import blogRoutes from './routes/blogRoutes.js';
import memberRoutes from './routes/memberRoutes.js'; // ‚úÖ Import member routes

dotenv.config();

const app = express();
const PORT = process.env.PORT || 3001;

// ‚úÖ Cloudinary ENV Check
console.log('üåê Cloudinary ENV Check:', {
  name: process.env.CLOUDINARY_CLOUD_NAME,
  key: process.env.CLOUDINARY_API_KEY,
  secret: process.env.CLOUDINARY_API_SECRET ? 'Exists ‚úÖ' : 'Missing ‚ùå',
});

// ‚úÖ CORS: Whitelist your frontend domains
const allowedOrigins = [
  'http://localhost:5173',
  'https://aicee-main.vercel.app',
  'https://aicee-main.onrender.com'
];

app.use(cors({
  origin: function (origin, callback) {
    if (!origin || allowedOrigins.includes(origin)) {
      callback(null, true);
    } else {
      console.warn('‚ùå CORS blocked:', origin);
      callback(new Error('Not allowed by CORS'));
    }
  },
  credentials: true
}));

// ‚úÖ Request body parsers
app.use(express.json({ limit: '100mb' }));
app.use(express.urlencoded({ extended: true, limit: '100mb' }));

// ‚úÖ API Routes
app.use('/api/blogs', blogRoutes);     // Blog-related routes (GET, POST, GET/:id)
app.use('/api/admin', memberRoutes);   // Member-related routes (/pending-members, /approve-member)

// ‚úÖ Root route
app.get('/', (req, res) => {
  res.send('‚úÖ AICEE Backend is running!');
});

// ‚úÖ 404 Handler (Optional, for catching undefined routes)
app.use((req, res, next) => {
  console.warn(`‚ùå Route not found: ${req.originalUrl}`);
  res.status(404).json({ error: `Route ${req.originalUrl} not found` });
});

// ‚úÖ MongoDB connection
mongoose.connect(process.env.MONGO_URI)
  .then(() => {
    console.log('‚úÖ MongoDB connected');
    app.listen(PORT, () => {
      console.log(`üöÄ Server running on http://localhost:${PORT}`);
    });
  })
  .catch((err) => {
    console.error('‚ùå MongoDB connection error:', err.message);
    process.exit(1); // Exit if DB fails
  });
